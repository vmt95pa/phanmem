    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String idNCC = "";
        String tenNCC = txtTenNhaCungCap.getText();
        String ghiChuPhieuNhap = "";
        if (nhaCungCapDangChon != null) {
            idNCC = nhaCungCapDangChon.getIdNCC();

        } else {
            String sdt, dc, ttKhac;
            sdt = "Số điện thoại: " + txtSdt.getText();
            dc = "Địa chỉ: " + txtDC.getText();
            ttKhac = txtTTkhac.getText();
            ghiChuPhieuNhap = sdt + "\n" + dc + "\n" + ttKhac;
        }
        String idPN = "PN-" + new Date().hashCode();
        dtoPhieuNhap pn = new dtoPhieuNhap(ghiChuPhieuNhap, idNCC, idPN, "Nhập hàng-" + tenNCC, tenNCC, new Date(), 0, 0, 0, 1, nhaCungCapDangChon, null);
        phieuNhapMoi = phieuNhapCtrl.ThemPhieuNhap(pn);
        if (phieuNhapMoi != null) {
            
            this.dispose();
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void tblChonNCCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblChonNCCMouseClicked
        nhaCungCapDangChon = (dtoNhaCungCap) tblChonNCC.getValueAt(tblChonNCC.getSelectedRow(), 2);
        setThongTinNCC();
        setStatusComponent(false);
        if (evt.getClickCount() == 2) {
            tabChonNhaCungCap.setSelectedIndex(0);

        }
    }//GEN-LAST:event_tblChonNCCMouseClicked


    public void OpenedForm() {
        RefreshData();
        System.out.println("Tổng các nhà cung cấp: " + nhaCungCapCtrl.ListNhaCungCap.size());
        nhaCungCapCtrl.FillChonNhaCungCap(tblChonNCC, nhaCungCapCtrl.ListNhaCungCap);

    }

    public void setThongTinNCC() {
        if (nhaCungCapDangChon != null) {
            txtDC.setText(nhaCungCapDangChon.getDc());
            txtKhuVuc.setText(nhaCungCapDangChon.getKhuVuc());
            txtMaNCC.setText(nhaCungCapDangChon.getIdNCC());
            txtNgayThem.setDate(nhaCungCapDangChon.getNgayThem());
            txtSdt.setText(nhaCungCapDangChon.getSdt());
            txtTTkhac.setText(nhaCungCapDangChon.getTtThem());
            txtTenNhaCungCap.setText(nhaCungCapDangChon.getTenNCC());

        }
    }

    public void setStatusComponent(boolean flag) {
        txtDC.setEnabled(flag);
        txtKhuVuc.setEnabled(flag);
//        txtMaNCC.setEnabled(flag);
        txtNgayThem.setEnabled(flag);
        txtNgayThem.setFocusCycleRoot(flag);
        txtSdt.setEnabled(flag);
        txtTTkhac.setEnabled(flag);
        txtTenNhaCungCap.setEnabled(flag);
        btnNhapKhongNCC.setEnabled(!flag);
    }


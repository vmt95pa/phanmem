    private void tblGioHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblGioHangMouseClicked
        int col = tblGioHang.columnAtPoint(evt.getPoint());
        int row = tblGioHang.getSelectedRow();

        dtoCTPhieuNhap ct = (dtoCTPhieuNhap) tblGioHang.getValueAt(row, 0);
        if (col == 3) {
            if (phieuNhapDangChon == null) {
                System.out.println("null");
                return;
            }
            if (ct != null) {

                if (XoaCTPhieuNhap(ct)) {
                    fillGioHang(tblGioHang, phieuNhapDangChon.getCtPhieuNhap());
                }

            }
        } else if (evt.getClickCount() == 2) {
            ThongTinSanPhamForm jdl = new ThongTinSanPhamForm(FormLoading.frm, true);
            dtoCTPhieuNhap ctPhieuNhap = (dtoCTPhieuNhap) tblGioHang.getValueAt(tblGioHang.getSelectedRow(), 0);
            jdl.sp = ctPhieuNhap.getSp();
            jdl.ctPhieuNhap = ctPhieuNhap;
            jdl.chon = 2;
            jdl.setLocationRelativeTo(tblGioHang);
            jdl.setVisible(true);
            if (jdl.ctPhieuNhap != null) {
                int indexCTPhieuNhap = phieuNhapDangChon.getCtPhieuNhap().indexOf(ctPhieuNhap);
                System.out.println("Index chi tiết phiếu nhập hiện tại: " + indexCTPhieuNhap);
                ctPhieuNhap = jdl.ctPhieuNhap;
                phieuNhapDangChon.getCtPhieuNhap().set(indexCTPhieuNhap, ctPhieuNhap);

                if (UpDatePhieuNhap(phieuNhapDangChon)) {
                    ThongBao.ThongBao("Đã sửa thông tin sản phẩm nhập", "Thông báo");
                    fillGioHang(tblGioHang, phieuNhapDaChon.getCtPhieuNhap());
                }

//            Sau khi đã sửa thông tin phiếu nhập
//            load lại bảng giỏ hàng
            }

        }
    }//GEN-LAST:event_tblGioHangMouseClicked


    public void SetThongTinPhieuNhap(int rowSelect) {
        phieuNhapDangChon = (dtoPhieuNhap) tblPhieuNhap.getValueAt(rowSelect, colPhieuNhapBangPhieuNhap);
        tabPhieuNhap.setSelectedIndex(0);
        txtConLai.setValue(phieuNhapDangChon.getTongTienConLai());
        txtDaThanhToan.setValue(phieuNhapDangChon.getSoTienTraTruoc());
        txtNgayNhap.setDate(phieuNhapDangChon.getNagyTaoPhieuNhap());

        txtTenPhieuNhap.setText(phieuNhapDangChon.getTenPhieuNhap());
        txtThongTinNCC.setText("Tên nhà cung cấp: " + phieuNhapDangChon.getTenNCC());

    }

    public double TinhTongTien() {
        List<dtoCTPhieuNhap> list = phieuNhapDangChon.getCtPhieuNhap();
        double tongTien = 0;
        for (dtoCTPhieuNhap ct : list) {
            tongTien += ct.getTongCTPhieuNhap();
        }
        return tongTien;
    }

